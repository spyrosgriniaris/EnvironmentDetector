package com.myhost.spyros.environmentdetector.Data;

public class OptionsSpinnerAdminData {
    private int icon;
    private String iconName;

    public OptionsSpinnerAdminData(int icon, String iconName) {
        this.icon = icon;
        this.iconName = iconName;
    }

    public int getIcon() {
        return icon;
    }

    public String getIconName() {
        return iconName;
    }
}
